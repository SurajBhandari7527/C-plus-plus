// WAP to check whether the given number is even or odd (By using if-else and conditional operator)
#include<iostream>
using namespace std;
int main(){int n;
    cout<<"Enter a number"<<endl;
    cin>>n;
    if(n%2==0){
        cout<<"It is even number"<<endl;
    }
    else{
        cout<<"It is odd number"<<endl;
    }
    return 0;
}